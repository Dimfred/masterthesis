import numpy as np

from tabulate import tabulate






########################################################################################
## LR
########################################################################################

res = {
    0.01: (83.71172, 84.14630, 85.10194),
    0.005: (81.63926, 82.84789, 82.40917),
    0.0025: (82.62219, 81.93978, 82.22474),
    0.001: (82.06907, 82.82420, 81.31691),
    0.0005: (81.57284, 82.18677, 81.60509),
    0.00025: (81.08143, 81.53399, 81.18215),
    0.0001: (79.58246, 79.76442, 79.82279),
}
res = sorted(res.items(), reverse=True)

pretty = [["LR", "Mean", "Std"]]
for lr, runs in res:
    runs = np.array(runs)
    pretty += [[lr, runs.mean(), runs.std()]]
print(tabulate(pretty))

